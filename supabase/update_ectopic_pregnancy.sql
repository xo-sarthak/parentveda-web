## What an ectopic pregnancy really is

A pregnancy normally makes its way to the uterus, an organ built to expand and nurture a baby for nine months. An ectopic pregnancy never reaches that destination. Instead it takes root somewhere it was never meant to be, in around 95 out of 100 cases inside the fallopian tube itself, and occasionally on an ovary, on the cervix, or within the scar of an earlier caesarean.

